// 메서드의 필수 구성요소
// 이름, 매개변수, 반환타입
// 반환타입 : void(반환하지 않는다)
package day07;
import java.util.*;
// 강사
public class MethodSub01 {
	void method1 () {
		System.out.println("method1");
	}
	void method2 (int i) {
		System.out.println("method2");
	}
	void method3 (int i, int j) {
		System.out.println("method3");
	}
	void method4 (String i) {
		System.out.println("method4");
	}
	void method5 (char i) {
		System.out.println("method5");
	}
	void method6 (Random r) {
		System.out.println("method6");
	}
	void method7 (Scanner sc) {
		System.out.println("method7");
	}
	
}
